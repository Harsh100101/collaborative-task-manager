import mongoose, { Schema, Document } from "mongoose";

export interface IUser extends Document {
	name: string;
	email: string;
	password: string;
}

const UserSchema = new Schema<IUser>(
	{
		name: {
			type: String,
			required: true,
			minlength: 2,
		},
		email: {
			type: String,
			required: true,
			unique: true,
			lowercase: true,
		},
		password: {
			type: String,
			required: true,
			minlength: 6,
		},
	},
	{ timestamps: true }
);

export const User = mongoose.model<IUser>("User", UserSchema);
